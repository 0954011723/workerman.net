<h3>2016-02-28</h3>
<p>workerman3.3.0版本发布，新增<a href = "http://doc3.workerman.net/appendices/about-frame.html">frame协议</a>。</p>
<p>发布全局变量共享组件<a href="http://doc3.workerman.net/component/global-data.html">GlobalData</a>(需要workerman>=3.3.0)，用于进程间/服务器间变量共享。</p>
<p>升级进程间/服务器间通讯组件<a href="http://doc3.workerman.net/component/channel.html">Channel</a>(需要workerman>=3.3.0)，使之支持二进制数据通讯。</p>

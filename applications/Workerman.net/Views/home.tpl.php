			什么是Workerman
		</h2>
		<p class="f14">
			Workerman是一个<strong>高性能</strong>的<strong>PHP socket 服务器框架</strong>。已经被多家公司用于Rpc服务器、聊天室服务器、网络游戏服务器、手机游戏等后台开发。
			只要会PHP，你就可以基于Workerman轻而易举的开发出你想要的网络应用，不必再为PHP Socket底层开发而烦恼。
		</p>
